let arr = [1,2,3,4,5]

console.log(arr.length) // length is not a function it is a property of array

arr.push(6) // add an element at the end of the array
console.log(arr)

arr.pop() // remove an element from the end of the array
console.log(arr)

arr.unshift(0) // add an element at the start of the array
console.log(arr)

arr.shift() // remove an element from the start of the array
console.log(arr)

arr.reverse() // reverse the array
console.log(arr)

console.log(arr.indexOf(5)) // find the index of an element in the array